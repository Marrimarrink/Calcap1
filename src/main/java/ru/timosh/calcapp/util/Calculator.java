package ru.timosh.calcapp.util;

public class Calculator {

    public static int addition (int x, int y) {
        int result = x + y;
        return result;
    }

    public static int subtraction (int x, int y) {
        int result = x - y;
        return result;
    }

    public static int multiplication (int x, int y) {
        int result = x * y;
        return result;
    }

}


